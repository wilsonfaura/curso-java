
public class Declaracion_operadores {

	public static void main(String[] args) {
		final double apulgadas=2.54;
		double centimetros=6;
		double resultado=centimetros/apulgadas;
		System.out.println("En "+ centimetros + " centimetros hay " + resultado+" pulgadas");
		int operador1, operador2, resultado2;
		operador1=9;
		operador2=7;
		resultado2=operador1+operador2;
		System.out.println(resultado2);
	}

}
